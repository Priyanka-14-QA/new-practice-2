
public class innerforloop {


			public static void main(String[] args) {
				
				int totalrow=5;
				for(int row=1;row<=totalrow;row++) {

				for(int st=1;st<=totalrow-row+1;st++) { 
					
					System.out.print("*");
					
				}
				
			System.out.println();
			
				
			
			
			}
			
			
			
			
			
			
			
			
			
			
			
			}

}


